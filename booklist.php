to generalize to human idea-space, but I doubt that a ‘complete’
list – categorizing all relevant books in a field, and having every
category of book – would be practical or efficient.</FONT></FONT></P>
<P STYLE="text-indent: 0.5in; margin-bottom: 0in"><FONT FACE="Times New Roman, serif"><FONT SIZE=3>This
